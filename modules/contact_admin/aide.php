    <h2>Questions fréquentes</h2>
        <p>
            <fieldset>
                <legend>Qui sommes nous?</legend>
                Homidays est une association à but non lucratif qui a pour objectif d'aider les particuliers, résidents en France, à échanger leur logement pour des vacances de courte durée (un jour à quelques semaines).
            </fieldset> 
        </p>
        <p>
            <fieldset>
                <legend>Pourquoi rejoindre Homidays?</legend>
                Vous pouvez effectuer autant d'échanges que vous le souhaitez tout en économisant sur votre budget de vacances. Vous pouvez également échanger vos voitures. Ces échanges sont également l’occasion d’une aventure humaine où vous établirez un rapport privilégié avec les échangistes. Certains d'entre elles deviendront même des amis.
            </fieldset>
        </p>
        <p>
            <fieldset>
                <legend>Est-ce sécuritaire de laisser ma maison à quelqu'un que je ne connais pas?</legend>
                Les échanges sont avant tout basés sur la confiance mutuelle. Vous devez prendre la peine de correspondre avec vos échangistes pour mieux les connaître et planifier les détails. Un échange bien planifié dans lequel chacun se sent respecté est habituellement un gage de réussite.
            </fieldset>
        </p>
        <p>
            <fieldset>
                <legend>Ma voiture est-elle assurée en cas d'échange?</legend>
                Nous vous conseillons d'être très vigilant, à savoir de déterminer avec votre assureur les responsabilités et de convenir avec la famille avec qui vous échangez, les modalités de paiement et la franchise en cas d'accident. Notez bien toutes les cassures avant votre départ.
            </fieldset>
        </p>
        <p>
            <fieldset>
                <legend>Puis-je échanger ma maison si je suis locataire?</legend>
                Bien sur, les locataires peuvent tout autant réaliser des échanges de maisons. Un échange de maisons n'est pas à confondre avec une sous-location.. Cependant, nous vous conseillons de prévenir le propriétaire ainsi que votre assureur. d'en aviser le propriétaire et votre assureur. Ils n'ont pas plus de raison de refuser que si vous prêtiez votre maison à des amis.
            </fieldset>
        </p>

